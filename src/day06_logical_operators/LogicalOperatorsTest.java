package day06_logical_operators;

public class LogicalOperatorsTest {

    public static void main(String[] args) {

        char c1 = 'A';
        char c2 = 65;
        int i1 = 5;
        int i2 = 10;

        boolean res1 = (i1==i2) & (c1==c2);
        boolean res2 = (i1==i2) & (c1==c2);


        System.out.println(res1);
        System.out.println(res2);


    }



}
