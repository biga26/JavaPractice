package day10_controlFlowStatements_4;

public class ForLoopTest {

    public static void main(String[] args) {

        for(int i=1;i<=10;i++) {
			System.out.println("i = " + i);
		}

        for(int i=5; i<=10; i++){
            System.out.println("i = " + i);
        }

        for(int i=10; i>=0; i--){
            System.out.println("i = " + i);
        }


    }

}
