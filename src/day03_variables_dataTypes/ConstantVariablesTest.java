package day03_variables_dataTypes;

public class ConstantVariablesTest {
    public static void main(String[] args) {

        int num1 = 10;
        System.out.println(num1);
        num1 = 20;
        System.out.println(num1);

        final int num2 = 100; //final cannot be changed
        System.out.println(num2);


    }
}
